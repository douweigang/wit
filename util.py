def get_new(new_str, old_str, old):
    """
    解决字符偏移，（css偏移）
    :param new_str: 混淆的字符顺序
    :param old_str: 网站正确显示的字符顺序
    :param old: 显示的错误字符串
    :return: 字符串
    """
    new = []
    for s in old:
        new.append(old_str[new_str.index(s)]) if s in new_str else new.append(s)
    return "".join(new)

